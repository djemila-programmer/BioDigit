class MainDashboard extends StatefulWidget {
  const MainDashboard({super.key});

  @override
  State<MainDashboard> createState() => _MainDashboardState();
}

class _MainDashboardState extends State<MainDashboard> {
  WeatherData? _weather;

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((_) {
      context.read<SensorProvider>().startListening();
      context.read<AlertProvider>().startListening();
      context.read<HistoryProvider>().loadProduction('weekly');
      _loadWeather();
    });
  }

  Future<void> _loadWeather() async {
    final weather = await WeatherService.getOuagaWeather();
    if (mounted) {
      setState(() => _weather = weather);
    }
  }

  @override
  Widget build(BuildContext context) {
    final auth = context.watch<AuthProvider>();
    final sensorProv = context.watch<SensorProvider>();
    final isFrench = context.watch<LocaleProvider>().isFrench;
    final cs = Theme.of(context).colorScheme;

    return Scaffold(
      backgroundColor: Theme.of(context).colorScheme.surface,
      appBar: _buildAppBar(context),
      body: SingleChildScrollView(
        padding: const EdgeInsets.fromLTRB(
          AppTheme.containerPadding,
          24,
          AppTheme.containerPadding,
          120,
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Hero Greeting
            _buildGreetingSection(auth.user),
            const SizedBox(height: 32),

            // Biodigester Visual
            const BiodigesterVisual(height: 220),
            const SizedBox(height: 32),

            _buildQuickActions(context),
            const SizedBox(height: 24),

            // ESP8266 Status Card
            const ESP8266StatusCard(),
            const SizedBox(height: 12),

            // Supabase Status Card
            const SupabaseStatusCard(),
            const SizedBox(height: 32),

            // Simulation indicator
            if (sensorProv.isSimulation)
              Container(
                width: double.infinity,
                margin: const EdgeInsets.only(bottom: 12),
                padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
                decoration: BoxDecoration(
                  color: const Color(0xFFFFF8E1),
                  borderRadius: BorderRadius.circular(8),
                  border: Border.all(color: const Color(0xFFF57F17).withValues(alpha: 0.3)),
                ),
                child: Row(
                  children: [
                    const Icon(Icons.science, size: 16, color: Color(0xFFF57F17)),
                    const SizedBox(width: 8),
                    Expanded(
                      child: Text(
                        isFrench ? 'Mode simulation - Données générées par le jumeau numérique' : 'Simulation mode - Data generated by digital twin',
                        style: const TextStyle(fontSize: 12, fontWeight: FontWeight.w500, color: Color(0xFFF57F17)),
                      ),
                    ),
                  ],
                ),
              ),

            // Sensor Grid (real-time from SensorProvider)
            Consumer<SensorProvider>(
              builder: (ctx, sensorProv, _) {
                final reading = sensorProv.latestReading;
                if (reading != null && reading.temperature > 0) {
                  return _buildLiveSensorGrid(reading);
                }
                if (sensorProv.isLoading) {
                  return const Padding(
                    padding: EdgeInsets.symmetric(vertical: 24),
                    child: Center(child: CircularProgressIndicator()),
                  );
                }
                return Padding(
                  padding: const EdgeInsets.symmetric(vertical: 24),
                  child: Center(
                    child: Column(
                      mainAxisSize: MainAxisSize.min,
                      children: [
                        Icon(Icons.sensors_off, size: 48, color: cs.onSurfaceVariant.withValues(alpha: 0.5)),
                        const SizedBox(height: 12),
                        Text(
                          isFrench ? 'Aucun capteur connecté' : 'No sensors connected',
                          style: TextStyle(
                            fontSize: 16,
                            fontWeight: FontWeight.w600,
                            color: cs.onSurface,
                          ),
                        ),
                        const SizedBox(height: 4),
                        Text(
                          isFrench ? 'Connectez votre ESP8266 pour voir les données en temps réel.' : 'Connect your ESP8266 to see real-time data.',
                          style: TextStyle(fontSize: 13, color: cs.onSurfaceVariant),
                          textAlign: TextAlign.center,
                        ),
                      ],
                    ),
                  ),
                );
              },
            ),
            const SizedBox(height: 32),

            // Biogas Production Card
            const BiogasProductionCard(),
            const SizedBox(height: 12),

            // Energy Impact Card
            const EnergyImpactCard(),
            const SizedBox(height: 32),

            // Daily Insight
            _buildDailyInsight(context),
          ],
        ),
      ),
      bottomNavigationBar: const BottomNavBar(currentIndex: 0),
    );
  }

  PreferredSizeWidget _buildAppBar(BuildContext context) {
    final cs = Theme.of(context).colorScheme;
    return PreferredSize(
      preferredSize: const Size.fromHeight(64),
      child: Container(
        color: Theme.of(context).colorScheme.surfaceContainerHighest,
        child: SafeArea(
          child: Padding(
            padding: const EdgeInsets.symmetric(
              horizontal: AppTheme.containerPadding,
              vertical: AppTheme.baseSpacing,
            ),
            child: Row(
              children: [
                const CircleAvatar(
                  radius: 20,
                  backgroundColor: AppTheme.primaryContainer,
                  child: Icon(Icons.eco, color: AppTheme.onPrimary, size: 20),
                ),
                const SizedBox(width: 12),
                const Text(
                  'BioDigit',
                  style: TextStyle(
                    fontSize: 22,
                    fontWeight: FontWeight.w500,
                    color: AppTheme.primary,
                  ),
                ),
                const Spacer(),
                IconButton(
                  onPressed: () =>
                      Navigator.pushNamed(context, AppRoutes.notifications),
                  icon: Icon(Icons.notifications, color: cs.onSurfaceVariant),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }

